function [inversa] = inversor_matriz(R, f_x)

inversa = zeros(1,3);
inversa(1,1) = (R(2,2)*R(3,3) - R(2,3)*R(3,2))*f_x(1,1) + (R(1,3)*R(3,2) - R(1,2)*R(3,3))*f_x(1,2) + (R(1,2)*R(2,3) - R(1,3)*R(2,2))*f_x(1,3);
inversa(1,2) = (R(2,3)*R(3,1) - R(2,1)*R(3,3))*f_x(1,1) + (R(1,1)*R(3,3) - R(1,3)*R(3,1))*f_x(1,2) + (R(1,3)*R(2,1) - R(1,1)*R(2,3))*f_x(1,3);
inversa(1,3) = (R(2,1)*R(3,2) - R(2,2)*R(3,1))*f_x(1,1) + (R(1,2)*R(3,1) - R(1,1)*R(3,2))*f_x(1,2) + (R(1,1)*R(2,2) - R(1,2)*R(2,1))*f_x(1,3);
